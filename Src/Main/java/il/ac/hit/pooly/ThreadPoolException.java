package il.ac.hit.pooly;

/**
 * The ThreadPoolException class represents an exception specific to the ThreadPool.
 */
public class ThreadPoolException extends Exception {
    /**
     * Constructs a new ThreadPoolException with the specified error message.
     * @param message The error message describing the exception.
     */
    public ThreadPoolException(String message) {
        super(message);
    }
}

